function generateClaimsPDF(data) {
  const { claims, members } = data

  let content = `
We Care Insurance - Claims Report
Generated: ${new Date().toLocaleString()}

CLAIMS SUMMARY
==============
Total Claims: ${claims.length}

CLAIMS DETAILS
==============
`

  claims.forEach((claim) => {
    const member = members.find((m) => m.id === claim.memberId)
    const memberName = member ? member.name : "Unknown"

    content += `
Claim ID: ${claim.id.slice(-8)}
Member: ${memberName}
Type: ${claim.claimType}
Amount: $${claim.amount}
Status: ${claim.status}
Date: ${new Date(claim.createdAt).toLocaleDateString()}
---
`
  })

  content += `
Report generated by We Care Insurance Management System
  `

  return content
}

