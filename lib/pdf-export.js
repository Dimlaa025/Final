function generateUsersPDF(data) {
  const { users } = data

  let content = `
We Care Insurance - Users Report
Generated: ${new Date().toLocaleString()}

USERS SUMMARY
=============
Total Users: ${users.length}

USERS BY ROLE
=============
`

  const roleCount = {}
  users.forEach((user) => {
    roleCount[user.role] = (roleCount[user.role] || 0) + 1
  })

  Object.entries(roleCount).forEach(([role, count]) => {
    content += `${role}: ${count}\n`
  })

  content += `

USERS DETAILS
=============
`

  users.forEach((user) => {
    content += `
Name: ${user.name}
Email: ${user.email}
Role: ${user.role}
Department: ${user.department}
Status: ${user.status}
---
`
  })

  content += `
Report generated by We Care Insurance Management System
  `

  return content
}

